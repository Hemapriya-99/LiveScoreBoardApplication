package com.sportapplication.model;


public class BowlerDetails {
	
	private String name;
	private String o;
	private String m;
	private String r;
	private String w;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getO() {
		return o;
	}
	public void setO(String o) {
		this.o = o;
	}
	public String getM() {
		return m;
	}
	public void setM(String m) {
		this.m = m;
	}
	public String getR() {
		return r;
	}
	public void setR(String r) {
		this.r = r;
	}
	public String getW() {
		return w;
	}
	public void setW(String w) {
		this.w = w;
	}
	
	


	
	

}
